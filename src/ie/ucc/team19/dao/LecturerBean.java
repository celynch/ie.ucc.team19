package ie.ucc.team19.dao;

public class LecturerBean {
    private int lecturer_id;
    private String first_name;
    private String last_name;
    private String email;
    private String lecturer_title;
    private String position;
    private String address_line1;
    private String address_line2;
    private String address_line3;
    private String country;
    private String telephone;

    public int getLecturer_id() {
        return lecturer_id;
    }
    public void setLecturer_id(int lecturer_id) {
        this.lecturer_id = lecturer_id;
    }
    public String getFirst_name() {
        return first_name;
    }
    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }
    public String getLast_name() {
        return last_name;
    }
    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }
    public String getEmail() {
        return email;
    }
    public void setEmail(String email) {
        this.email = email;
    }
    public String getLecturer_title() {
        return lecturer_title;
    }
    public void setLecturer_title(String lecturer_title) {
        this.lecturer_title = lecturer_title;
    }
    public String getPosition() {
        return position;
    }
    public void setPosition(String position) {
        this.position = position;
    }
    public String getAddress_line1() {
        return address_line1;
    }
    public void setAddress_line1(String address_line1) {
        this.address_line1 = address_line1;
    }
    public String getAddress_line2() {
        return address_line2;
    }
    public void setAddress_line2(String address_line2) {
        this.address_line2 = address_line2;
    }
    public String getAddress_line3() {
        return address_line3;
    }
    public void setAddress_line3(String address_line3) {
        this.address_line3 = address_line3;
    }
    public String getCountry() {
        return country;
    }
    public void setCountry(String country) {
        this.country = country;
    }
    public String getTelephone() {
        return telephone;
    }
    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }
}
